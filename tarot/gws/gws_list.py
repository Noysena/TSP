    home = os.path.expanduser('~')
    filegws = os.path.join(home, filetxt)
    
    if os.path.isfile(filegws):
        with open(filegws, 'r') as rg:
            gwslist = rg.readlines()
    else:
        filegws = os.path.join(home, 'Documents', filetxt)
        with open(filegws, 'r') as rg:
            gwslist = rg.readlines()
                
    gws_folder = [x.rstrip() for x in gwslist]
    gws_name = [x.replace('_', '') for x in gws_folder]
    
    return gws_folder, gws_name

